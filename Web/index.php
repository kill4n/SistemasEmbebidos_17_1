				<p style="text-align:justify"> <strong>El sistema de monitoreo</strong>, permite observar algunas características climáticas de los cultivos de papa de una finca ubicada en la sabana de Bogotá o entre los límites de los municipios de Mosquera, Facatativá, Funza, Madrid, Tenjo y El Rosal. Esta zona, ubicada sobre la cordillera oriental en la región central del departamento de Cundinamarca hace parte del Altiplano Cundiboyacense. <br/><br/>
Es de tener en cuanta, que el paisaje de la Sabana se caracteriza por su clima frío, con fuertes contrastes de tiempo soleado y lluvioso. Lo cual en ocasiones cuando se presentan heladas, dificultan la continuidad y éxito de todos los cultivos que no se realicen bajo invernadero, por lo que son comunes los cultivos de flores y hortalizas con este tipo de tecnologías. La contaminación biológica y química del rio Bogotá, disminuye la calidad de los productos que se riegan con sus aguas y es necesario hacer uso de aguas subterráneas o de algunas de las corrientes pequeñas que todavía tienen bajos niveles de contaminación (Carrizosa, 2007).(Oswaldo & Barbosa, 2011)<br/><br/>
Teniendo en cuenta esta descripción, cabe mencionar que algunas de las características generales para el cultivo óptimo de la papa se destaca un ambiente de temperatura promedio de 10º a 20º centígrados, con una precipitación promedio de 900mm de lluvia al año o superior, asimismo un suelo con textura suelta y profunda que evite la acumulación de humedad en la raíz, y un PH entre 5.2 y 5.9 además de un alto contenido de materia orgánica (Gómez Susa, 2014). Con esto se plantea que el sistema de monitoreo reconozca y mida variable como la temperatura, la presión, la humedad y la luminosidad empleando cuatro módulos distribuidos en la finca. 
  </p><br/><br/>
			
